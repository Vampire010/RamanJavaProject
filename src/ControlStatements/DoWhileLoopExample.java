package ControlStatements;

public class DoWhileLoopExample {

	public static void main(String[] args) 
	{
		//DoWhileLoopExample
		int i = 1;
        do {
            System.out.println("Value of i is: " + i);
            i++;
        }
        while (i <= 5); //Condition is checked after the loop body executes.

	}

}
